# 8. Faça um Programa que pergunte quanto você ganha por hora e o número de horas trabalhadas no mês. Calcule e mostre o
# total do seu salário no referido mês.

hora = float ( input ('Digite a quantidade de horas trabalhada no mês: '))
salario = float ( input ('Digite a quantidade do valor por hora: '))

calc = hora * salario

print ('O valor do salário por mês é: ', calc)